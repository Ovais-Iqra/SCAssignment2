using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Calculator_App
{
    class Calculator_Functions
    {
        public double addFunction(double val1, double val2)
        {
            return val1 + val2;
        }
        public double subFunction(double val1, double val2)
        {
            return val1 - val2;
        }
        public double mulFunction(double val1, double val2)
        {
            return val1 * val2;
        }
        public double divFunction(double val1, double val2)
        {
            return val1 / val2;
        }
    }
}
